updateNavbar(); /* run once on load */


/* ============================================================
   HAMBURGER MENU
   ============================================================ */
const hamburger = document.getElementById('hamburger');
const navMenu   = document.getElementById('navMenu');

hamburger.addEventListener('click', () => {
  const isOpen = navMenu.classList.toggle('open');
  hamburger.classList.toggle('open', isOpen);
  hamburger.setAttribute('aria-expanded', String(isOpen));
});

/* Close menu on nav-link click */
navMenu.querySelectorAll('.nav-link').forEach(link => {
  link.addEventListener('click', () => {
    navMenu.classList.remove('open');
    hamburger.classList.remove('open');
    hamburger.setAttribute('aria-expanded', 'false');
  });
});

/* Close menu on outside click */
document.addEventListener('click', e => {
  if (!navbar.contains(e.target)) {
    navMenu.classList.remove('open');
    hamburger.classList.remove('open');
    hamburger.setAttribute('aria-expanded', 'false');
  }
});


/* ============================================================
   SCROLL ANIMATIONS — fade-in on enter
   ============================================================ */
const fadeObserver = new IntersectionObserver((entries) => {
  entries.forEach((entry, i) => {
    if (!entry.isIntersecting) return;
    /* stagger siblings in the same batch */
    setTimeout(() => {
      entry.target.classList.add('visible');
    }, i * 90);
    fadeObserver.unobserve(entry.target);
  });
}, {
  threshold: 0.1,
  rootMargin: '0px 0px -48px 0px',
});

document.querySelectorAll('.fade-in').forEach(el => fadeObserver.observe(el));


/* ============================================================
   LIGHTBOX — diploma image viewer
   ============================================================ */
(function initLightbox() {
  const lightbox = document.getElementById('lightbox');
  const overlay  = document.getElementById('lightboxOverlay');
  const closeBtn = document.getElementById('lightboxClose');
  const thumb    = document.getElementById('diplomaThumb');
  if (!lightbox || !thumb) return;

  function open() {
    lightbox.classList.add('open');
    document.body.style.overflow = 'hidden';
  }

  function close() {
    lightbox.classList.remove('open');
    document.body.style.overflow = '';
  }

  thumb.addEventListener('click', open);
  overlay.addEventListener('click', close);
  closeBtn.addEventListener('click', close);
  document.addEventListener('keydown', e => {
    if (e.key === 'Escape') close();
  });
})();


/* ============================================================
   HERO CANVAS — Data network animation
   ============================================================ */
(function initHeroCanvas() {
  if (window.matchMedia('(prefers-reduced-motion: reduce)').matches) return;

  const canvas = document.getElementById('heroCanvas');
  if (!canvas) return;
  const ctx = canvas.getContext('2d');

  const ACCENT    = '59,130,246';
  const N_NODES   = 52;
  const DIST      = 145;    /* max connection distance */

  /* Subtle data-world labels that float with some nodes */
  const LABELS = ['SQL', 'KPI', 'DAX', 'P&L', '%', 'Σ', '↗', '$', '≈', 'ROI', 'Δ', 'EDA'];

  let nodes = [];
  let W, H, raf;

  /* ---- resize ---- */
  function resize() {
    W = canvas.width  = canvas.offsetWidth;
    H = canvas.height = canvas.offsetHeight;
  }

  /* ---- node factory ---- */
  function makeNode() {
    return {
      x:  Math.random() * W,
      y:  Math.random() * H,
      vx: (Math.random() - 0.5) * 0.28,
      vy: (Math.random() - 0.5) * 0.28,
      r:  Math.random() * 1.6 + 0.8,
      baseOp: Math.random() * 0.25 + 0.12,
      pulse:  Math.random() * Math.PI * 2,
      pSpeed: 0.007 + Math.random() * 0.011,
      label:  Math.random() < 0.20
                ? LABELS[Math.floor(Math.random() * LABELS.length)]
                : null,
      lPhase: Math.random() * Math.PI * 2,
    };
  }

  /* ---- init ---- */
  function init() {
    resize();
    nodes = Array.from({ length: N_NODES }, makeNode);
  }

  /* ---- draw loop ---- */
  function draw() {
    ctx.clearRect(0, 0, W, H);

    /* move nodes, wrap edges */
    for (const n of nodes) {
      n.x += n.vx;
      n.y += n.vy;
      n.pulse += n.pSpeed;
      if (n.x < -24) n.x = W + 24;
      else if (n.x > W + 24) n.x = -24;
      if (n.y < -24) n.y = H + 24;
      else if (n.y > H + 24) n.y = -24;
    }

    /* connections */
    for (let i = 0; i < nodes.length; i++) {
      for (let j = i + 1; j < nodes.length; j++) {
        const a = nodes[i], b = nodes[j];
        const dx = a.x - b.x, dy = a.y - b.y;
        const d2 = dx * dx + dy * dy;
        if (d2 < DIST * DIST) {
          const alpha = (1 - Math.sqrt(d2) / DIST) * 0.11;
          ctx.beginPath();
          ctx.strokeStyle = `rgba(${ACCENT},${alpha.toFixed(3)})`;
          ctx.lineWidth   = 0.7;
          ctx.moveTo(a.x, a.y);
          ctx.lineTo(b.x, b.y);
          ctx.stroke();
        }
      }
    }

    /* nodes + labels */
    for (const n of nodes) {
      const sinP = Math.sin(n.pulse);
      const r    = n.r + sinP * 0.45;
      const op   = n.baseOp + sinP * 0.07;

      ctx.beginPath();
      ctx.arc(n.x, n.y, r, 0, Math.PI * 2);
      ctx.fillStyle = `rgba(${ACCENT},${op.toFixed(3)})`;
      ctx.fill();

      if (n.label) {
        /* label fades in/out on its own slower sine */
        const lOp = (n.baseOp * 0.9) * (0.5 + 0.5 * Math.sin(n.pulse * 0.38 + n.lPhase));
        ctx.font      = '8.5px Inter, sans-serif';
        ctx.fillStyle = `rgba(${ACCENT},${lOp.toFixed(3)})`;
        ctx.fillText(n.label, n.x + r + 3, n.y + 3);
      }
    }

    raf = requestAnimationFrame(draw);
  }

  /* ---- responsive ---- */
  window.addEventListener('resize', () => {
    resize();
    for (const n of nodes) {
      /* keep nodes in-bounds after resize */
      n.x = Math.max(0, Math.min(n.x, W));
      n.y = Math.max(0, Math.min(n.y, H));
    }
  }, { passive: true });

  /* pause when hero is out of view (perf) */
  const heroSection = document.getElementById('hero');
  const visObserver = new IntersectionObserver(entries => {
    if (entries[0].isIntersecting) {
      if (!raf) raf = requestAnimationFrame(draw);
    } else {
      cancelAnimationFrame(raf);
      raf = null;
    }
  }, { threshold: 0 });
  visObserver.observe(heroSection);

  init();
  raf = requestAnimationFrame(draw);
})();


/* ============================================================
   CONTACT FORM — visual feedback only (no backend)
   ============================================================ */
document.getElementById('contactForm').addEventListener('submit', e => {
  e.preventDefault();
  const btn  = e.target.querySelector('button[type="submit"]');
  const orig = translations[currentLang]['contact.form.send'];
  const sent = translations[currentLang]['contact.form.sent'];

  btn.textContent         = sent;
  btn.style.background    = '#10B981';
  btn.style.pointerEvents = 'none';

  setTimeout(() => {
    btn.textContent         = orig;
    btn.style.background    = '';
    btn.style.pointerEvents = '';
    e.target.reset();
  }, 2800);
});
